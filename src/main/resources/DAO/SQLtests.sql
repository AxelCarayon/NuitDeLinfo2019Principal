CREATE TABLE TUTEUR
(
    mail VARCHAR(100) NOT NULL PRIMARY KEY,
    sexe VARCHAR(1),
    description VARCHAR(255) NOT NULL
);


CREATE TABLE NOTES
(
    id_notes INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    note INT NOT NULL,
    mail varchar(100) NOT NULL REFERENCES TUTEUR(MAIL)
);


CREATE TABLE INTERETS
(
    id_interets INT GENERATED BY DEFAULT AS IDENTITY NOT NULL  PRIMARY KEY,
    Categorie VARCHAR(100) NOT NULL,
    mail VARCHAR(100) NOT NULL REFERENCES TUTEUR(MAIL)
);


